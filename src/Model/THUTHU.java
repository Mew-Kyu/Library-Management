package Model;

/**
 *
 * @author nhlon
 */
public class THUTHU {
    String ACC;
    String PASS;

    public THUTHU() {
    }

    public THUTHU(String ACC, String PASS) {
        this.ACC = ACC;
        this.PASS = PASS;
    }

    public String getACC() {
        return ACC;
    }

    public void setACC(String ACC) {
        this.ACC = ACC;
    }

    public String getPASS() {
        return PASS;
    }

    public void setPASS(String PASS) {
        this.PASS = PASS;
    }
    
}
